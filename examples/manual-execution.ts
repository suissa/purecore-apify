export async function executarPassoAPasso() {
  console.log('🚀 Executando passo a passo...\n');

  console.log('📂 Etapa 1: Criando instância do gerador...');
  const generator = new AutoGeneratorDDD({
    modulesPath: 'examples',
    domainPath: 'src/domain',
    boundedContext: 'user',
    verbose: true,
    force: true,
    dryRun: true
  });

  console.log('🔍 Etapa 2: Procurando schemas Zod...');
  // O generate() faz automaticamente

  console.log('🏗️  Etapa 3: Gerando estrutura DDD...');
  await generator.generate();

  console.log('📊 Etapa 4: Verificando módulos gerados...');
  const modulos = AutoGeneratorDDD.listGeneratedDDDModules('examples');
  console.log(`📦 Módulos encontrados: ${modulos.join(', ')}`);

  console.log('✅ Execução passo a passo concluída!');
}

