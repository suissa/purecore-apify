// =========================================
// MÉTODO 1: Usando a função utilitária
// =========================================

export async function executarGeradorSimples() {
  console.log('🚀 Executando via função utilitária...\n');

  await autoGenerateDDDFromZodSchemas({
    modulesPath: 'examples',           // ← Procura schemas aqui
    domainPath: 'src/domain',          // ← Gera domínio aqui
    boundedContext: 'user',            // ← Nome do contexto
    verbose: true,                     // ← Log detalhado
    force: true,                       // ← Sobrescrever arquivos
    dryRun: true                       // ← Apenas simular (mude para false)
  });

  console.log('✅ Função utilitária executada!');
}

// =========================================
// MÉTODO 2: Usando a classe diretamente
// =========================================

export async function executarGeradorClasse() {
  console.log('🚀 Executando via classe...\n');

  const generator = new AutoGeneratorDDD({
    modulesPath: 'examples',
    domainPath: 'src/domain',
    boundedContext: 'user',
    verbose: true,
    force: true,
    dryRun: true
  });

  await generator.generate();

  console.log('✅ Classe executada!');
}

